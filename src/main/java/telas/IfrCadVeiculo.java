    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaInsert()) {
            Session sessao = null;
            try {
                sessao = HibernateUtil.getSessionFactory().openSession();
                Transaction transacao = sessao.beginTransaction();
                Veiculo veiculo = new Veiculo();
                veiculo.setPlaca(txtPlaca.getText());
                veiculo.setKml(Double.parseDouble(txtKml.getText()));
                veiculo.setValor_disp_km(Double.parseDouble(txtvalor_disp_km.getText()));
                veiculo.setMarca(txtMarca.getText());
                veiculo.setModelo(txtModelo.getText());
                veiculo.setCor(txtCor.getText());

                if (id > -1) {
                    veiculo.setId_veiculo(id);
                    sessao.update(veiculo);
                } else {
                    sessao.save(veiculo);
                }

                transacao.commit();
                JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!");
            } catch (HibernateException hibEx) {
                hibEx.printStackTrace();
            } finally {
                sessao.close();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Favor, verifique os dados");
            errors = "";
        }

    }//GEN-LAST:event_btnSalvarActionPerformed



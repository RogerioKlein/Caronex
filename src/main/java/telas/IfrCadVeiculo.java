    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        id = Integer.parseInt(String.valueOf(tblVeiculos.getValueAt(tblVeiculos.getSelectedRow(), 0)));
        jTabbedPane1.setSelectedIndex(0);
        Session sessao = null;
        sessao = HibernateUtil.getSessionFactory().openSession();
        try {

            Transaction transacao = sessao.beginTransaction();

            Veiculo veiculo = (Veiculo) sessao.get(Veiculo.class, id);
            txtPlaca.setText(veiculo.getPlaca());
            txtKml.setText(String.valueOf(veiculo.getKml()));
            txtvalor_disp_km.setText(String.valueOf(veiculo.getValor_disp_km()));
            txtMarca.setText(String.valueOf(veiculo.getMarca()));
            txtModelo.setText(veiculo.getModelo());
            txtCor.setText(String.valueOf(veiculo.getCor()));

        } catch (HibernateException hibEx) {
            hibEx.printStackTrace();
        } finally {
            sessao.close();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

